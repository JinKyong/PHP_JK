<?PHP
/* for문 예제 : 섭씨를 화씨로 변환
 * -15, -10, -5, ..., 30, 35의 섭씨를 화씨로 변환해서 출력
 * 섭씨 -> 화씨 변환 공식 : °F = °C × 1.8 + 32 -> 구글 검색
 *
 * HTML에서 띄워쓰기 : &nbsp; 로 표시
 */

echo "----------------------------------------<br>";
echo "&nbsp;&nbsp;&nbsp;섭씨&nbsp;&nbsp;&nbsp;화씨<br>";
echo "----------------------------------------<br>";

for ($c=-15; $c<=35; $c=$c+5)
{
	$f = $c * 1.8 + 32;
	echo "&nbsp;&nbsp;&nbsp; $c &nbsp;&nbsp;&nbsp; $f<br>";
}

echo "----------------------------------------<br>";
?>


